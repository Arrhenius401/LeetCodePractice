package Classic150.Bitwise;

//颠倒给定的 32 位有符号整数的二进制位。

public class problem2 {
    public int reverseBits(int n) {
        int res = 0;
        // 循环条件：处理32位（因为int是32位），且原数n不为0（提前结束优化）
        for (int i = 0; i < 32 || n != 0; i++) {
            // 1. 取n的最低位（n & 1：若n为奇数则结果为1，偶数为0）
            // 2. 将这个最低位左移(31 - i)位，移动到反转后的对应位置
            // 3. 用 | 操作将该位合并到结果rev中（不影响已处理的位）
            res |= (n & 1) << (31 - i);

            // 将n无符号右移1位（>>> 确保高位补0，处理无符号整数逻辑）
            // 目的是把刚处理完的最低位“去掉”，准备处理下一位
            n >>>= 1;
        }
        return res;
    }
}
